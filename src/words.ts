export interface WordEntry {
  ko: string
  category: string
  emoji: string
}

// 평탄화 + 중복 제거(처음 등장한 카테고리 기준)
const seen = new Set<string>()
export const WORD_POOL: WordEntry[] = []
for (const c of CATEGORIES) {
  for (const w of c.words) {
    if (!seen.has(w)) {
      seen.add(w)
      WORD_POOL.push({ ko: w, category: c.name, emoji: c.emoji })
    }
  }
}

/** 풀에서 단어 하나를 랜덤으로 고른다. (직전 단어와 다르게) */
export function pickRandomWord(excludeKo?: string): WordEntry {
  const candidates =
    excludeKo && WORD_POOL.length > 1 ? WORD_POOL.filter((w) => w.ko !== excludeKo) : WORD_POOL
  return candidates[Math.floor(Math.random() * candidates.length)]
}

/** 단어를 음절(글자) 단위로 분리. 예: "떡볶이" → ["떡","볶","이"] */
export function toSyllables(word: string): string[] {
  return Array.from(word)
}

/** 단어의 사진 + 사전적 설명을 위키백과에서 가져온다. (없으면 null 필드) */
export interface WordInfo {
  extract: string | null
  thumb: string | null
}
export async function fetchWordInfo(word: string): Promise<WordInfo> {
  try {
    const r = await fetch(`https://ko.wikipedia.org/api/rest_v1/page/summary/${encodeURIComponent(word)}`)
    if (!r.ok) return { extract: null, thumb: null }
    const j: any = await r.json()
    if (j.type === 'disambiguation') return { extract: null, thumb: null }
    return { extract: j.extract || null, thumb: j.thumbnail?.source || null }
  } catch {
    return { extract: null, thumb: null }
  }
}
